/*
 * Convert integer to a hex-string in reverse.  Works for any number of bits
 */
#define HEX_STR_SIZE(type) (sizeof(type)*2)
#define NIB_TO_HEX(n) ("0123456789abcdef"[n])
#define HEX_CVT(i,b) \
    { \
	int cnt = HEX_STR_SIZE(i); \
	do { \
	    *(b)++ = NIB_TO_HEX((i)&15); \
	    (i) = (i) >> 4; \
	} while (i && --cnt); \
    }

